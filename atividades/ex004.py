n = input('Digite algo: ')

print('The primitive type of this value is: ', type(n))

print('Are there only spaces? ', n.isspace())

print('Is it a number? ', n.isnumeric())

print('Is it alphabetical? ', n.isalpha())

print('Is it alphanumeric? ', n.isalnum())

print(' Is it in uppercase?', n.isupper())

print('Is it in lowercase? ', n.islower())

print('Is it capitalized? ', n.istittle())

